import math
print(math.ceil(2.7))  # Mostra o maior inteiro menor ou igual a 2.7
print(math.floor(2.7))  # Mostra o menor inteiro maior ou igual a 2.7
print(math.pi)  # Mostra o valor de pi
print(math.e)  # Mostra o valor de e
print(math.sqrt(25))  # Mostra a raiz quadrada de 25
print(math.pow(2, 3))  # Mostra 2 elevado a 3
print(math.log(100, 10))  # Mostra o logaritmo de 100 em base 10
print(math.log10(100))  # Mostra o logaritmo de 100 em base 10
print(math.sin(math.pi / 2))  # Mostra o seno de 90 graus
print(math.cos(math.pi / 2))  # Mostra o cosseno de 90 graus
print(math.tan(math.pi / 4))  # Mostra a tangente de 45 graus
print(math.asin(0.5))  # Mostra o arcoseno de 0.5
print(math.acos(0.5))  # Mostra o arcocoseno de 0.5
print(math.atan(0.5))  # Mostra o arctangente de 0.5
print(math.degrees(math.pi))  # Mostra o valor de pi em graus
print(math.factorial(5))  # Mostra o valor de 180!